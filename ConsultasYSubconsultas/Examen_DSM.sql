FROM EJEMPLARESMARIPOSAS e JOIN ZONLOCPRO z 
ON E.NOMBRE_ZONA = Z.NOMBRE_ZONA
GROUP BY Z.LOCALIDAD , Z.PROVINCIA 
HAVING COUNT(E.NOMBRE_ESPECIE)>6
ORDER BY COUNT(E.NOMBRE_ESPECIE);





						