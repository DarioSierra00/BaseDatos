		FROM AFICIONADOS a, EJEMPLARESMARIPOSAS e
		WHERE A.DNI=E.DNI_CAPTURA
		GROUP BY A.NOMBRE,A.APELLIDOS
		ORDER BY COUNT(E.NOMBRE_ESPECIE) DESC)
WHERE ROWNUM=1;

